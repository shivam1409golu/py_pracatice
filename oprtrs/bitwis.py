# Bitwise Operator:-
# => it is used to perform bit-level operations on binary numbers.
# # Example:- &, |, ^, ~


a = 10  # In binary: 1010
b = 4   # In binary: 0100
print(a & b)  # Bitwise AND: 0000 => 0

print(a | b)  # Bitwise OR: 1110 => 14

print(a ^ b)  # Bitwise XOR: 1110 => 14

print(~a)  # Bitwise NOT: -(1011) => -11