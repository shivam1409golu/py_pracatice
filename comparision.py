# Comparison Operator
# => it is used to compare two values and returns a boolean result.

# Example:- ==, !=, >, <, >=, <=

a = 5
b = 5
print(a < b)  # True
print(a > b)  # False
print(a == b)  # True
print("check a != b: ", a != b)  # False
print("check less than or equal to : ", a <= b)  # True

# a less than or equal to b
print(a >= b)  # True
